#include "utils.h"

int	is_space(char ch)
{
	return (ch == ' '
		|| ch == '\n'
		|| ch == '\r'
		|| ch == '\t'
		|| ch == '\f'
		|| ch == '\v');
}

int	is_digit(char ch)
{
	return (ch >= '0' && ch <= '9');
}
